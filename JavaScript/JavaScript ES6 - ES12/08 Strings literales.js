
// la contra aspa "` `" se utiliza como delimitador, ej: `soy un string`

// el simbolo del dolar $ se utiliza para renderizar las variables, ej: `${nombreVariable}`

let user = "Kevin";
console.log(`Hola ${user}!`); // Hola Kevin!

// es una manera mas corta de escribir:
console.log("Hola " + user + "!");



